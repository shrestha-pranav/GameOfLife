function setGrid(table, mode){
	var arr = presetGrid(mode)
	var w = arr.length
	var l = arr[0].length
	var x1 = Math.floor(49- w/2)
	var y1 = Math.floor((100-l)/2)
	for(var i = 0;i<w;i++)
		for(var j = 0;j<l;j++)
			table.rows[i+x1].cells[j+y1].dataset.val = arr[i][j];
	paint(table);
	console.log("Yolo");
}